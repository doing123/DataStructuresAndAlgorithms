/**
 * @param {number[]} nums
 */
var NumArray = function (nums) {
  this.arr = nums;
};

/**
 * @param {number} i
 * @param {number} val
 * @return {void}
 */
NumArray.prototype.update = function (i, val) {
  this.arr[i] = val;
};

/**
 * @param {number} i
 * @param {number} j
 * @return {number}
 */
NumArray.prototype.sumRange = function (i, j) {
  let sum = 0;
  while (i <= j && i < this.arr.length) {
    sum += this.arr[i];
    i++;
  }
  return sum;
};

